    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String enteredId = jTextField1.getText().trim();
        String enteredPassword = new String(jPasswordField1.getPassword()).trim();
        
        if(enteredId.isEmpty() || enteredPassword.isEmpty()){
        JOptionPane.showMessageDialog(this, "Please enter Company ID and Password");
        return;
    }
    
    boolean isAuthenticated = false;
    String profilePicPath = null;
    
    try{
        BufferedReader br = new BufferedReader(new FileReader("CompanyDetails.csv"));
        String line;
        while ((line = br.readLine()) != null){
            String[] companyDetails =line.split(",");
            
            
            if (companyDetails[0].trim().equals(enteredId) && companyDetails[3].trim().equals(enteredPassword)){
                
                if(companyDetails[9].trim().equalsIgnoreCase("approved")){
                isAuthenticated = true;
                currentCompanyId = enteredId;
                profilePicPath = companyDetails[8];
                }
                else {
                    JOptionPane.showMessageDialog(this, "Your company is not approved yet. Plase check again later.");
                    return;
                }
                break;
            }
        }
    br.close();
    
    if (isAuthenticated) {
        EMPProfile empProfilePage = new EMPProfile(profilePicPath, currentCompanyId); // Assuming this is the next page to navigate to
        empProfilePage.setVisible(true);
        this.dispose();
    } else {
        jLabel3.setText("Password or Company ID is incorrect");
    }
    } catch (IOException e){
        e.printStackTrace();
    }

    }//GEN-LAST:event_jButton1ActionPerformed

