    private void loadCredentialsFromCSV(){
        String csvFile="CompanyDetails.csv";
        String line;
        String csvSplitBy=",";
        
        try (BufferedReader br = new BufferedReader(new FileReader(csvFile))){
            while ((line=br.readLine()) !=null){
                String[] details = line.split(csvSplitBy);
                String Company_ID = details[0].trim();
                String CPassword = details[3].trim();
                credentialsMap.put(Company_ID,CPassword);
                System.out.println("Loaded Company_ID: " + Company_ID + ", Password: " + CPassword);
            }
        } catch (IOException e){
            e.printStackTrace();
        }
    }
    
    private boolean authenticUser(String id, String password){
        System.out.println("Entered ID: "+id);
        System.out.println("Entered Password: "+password);
        
        if (credentialsMap.containsKey(id)){
            String storedPassword = credentialsMap.get(id);
            System.out.println("Stored Password: "+storedPassword);
            return storedPassword.equals(password);
            
            
        } else{
            System.out.println("ID not found in credentialsMap.");
            return false;
        }
        
    }
    
    
